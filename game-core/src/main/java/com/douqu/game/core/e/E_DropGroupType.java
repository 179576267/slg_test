public enum E_DropGroupType implements BaseEnum {

    SINGLE(1,"根据各自的掉掉落类型掉落\n"),
    WHOLE(2,"道具,资源,卡牌综合掉落一个")

    ;

    int code;

    String msg;

    E_DropGroupType(int code, String msg)
    {
        this.code = code;
        this.msg = msg;
    }

    @Override
    public int getCode() {
        return code;
    }

    @Override
    public String getMsg() {
        return msg;
    }
}
