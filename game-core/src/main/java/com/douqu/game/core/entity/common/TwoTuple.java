package com.douqu.game.core.entity.common;

/**
 * 二元元祖
 * @author FXW
 * 2016年6月24日
 */
public class TwoTuple<A,B> {

	private A first;
	private B second;

	public TwoTuple() {
	}

	/**
	 * @param first
	 */
	public TwoTuple(A first)
	{
		super();

		this.first = first;
	}

	/**
	 * @param first
	 * @param second
	 */
	public TwoTuple(A first, B second)
	{
		super();

		this.first = first;
		this.second = second;
	}

	public A getFirst() {
		return first;
	}

	public B getSecond() {
		return second;
	}

	public void setFirst(A first) {
		this.first = first;
	}

	public void setSecond(B second) {
		this.second = second;
	}

	public boolean isNull()
	{
		return first == null && second == null;
	}


	@Override
	public String toString() {
		return "TwoTuple{" +
				"first=" + first +
				", second=" + second +
				'}';
	}
}
