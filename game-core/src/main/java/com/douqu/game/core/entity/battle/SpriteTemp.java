package com.douqu.game.core.entity.battle;

import com.douqu.game.core.entity.EntityObject;

/**
 * @Author: Bean
 * @Description:
 * @Date: 2017-12-06 14:21
 */
public class SpriteTemp extends EntityObject {

    /**
     * 对应配置文件的ID
     * 主将时是Master表
     * 士兵时是Card表
     */
    private int id;

    private int hp;

    private int maxHp;

    public SpriteTemp(int id, int hp, int maxHp)
    {
        this.id = id;
        this.hp = hp;
        this.maxHp = maxHp;
    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getMaxHp() {
        return maxHp;
    }

    public void setMaxHp(int maxHp) {
        this.maxHp = maxHp;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "{" +
                "id=" + id +
                ", hp=" + hp +
                ", maxHp=" + maxHp +
                "} ";
    }
}
