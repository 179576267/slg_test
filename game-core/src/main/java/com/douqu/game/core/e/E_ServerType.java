public enum E_ServerType implements BaseEnum{

    BATTLE(1,"TCP战斗服务器"),
    GM(2,"GM连接"),
    ROUTE(3,"路线服务器")
    ;

    int code;
    String msg;

    E_ServerType(int code, String msg)
    {
        this.code = code;
        this.msg = msg;
    }

    @Override
    public int getCode() {
        return code;
    }

    @Override
    public String getMsg() {
        return msg;
    }
}
