public class Vector2
{
    public float x;
    public float y;

    public Vector2(float x, float y)
    {
        this.x = x;
        this.y = y;
    }

    public Vector2(Grid g)
    {
        this.x = g.x;
        this.y = g.y;
    }
    public String toString()
    {
        return "x:"+this.x+ "   y:"+this.y;
    }
}
