public enum E_BattleResult implements BaseEnum {

    WIN(1,"胜利"),
    DOGFALL(2,"平局"),
    LOSE(3,"失败")

    ;

    int code;

    String msg;

    E_BattleResult(int code, String msg)
    {
        this.code = code;
        this.msg = msg;
    }

    @Override
    public int getCode() {
        return code;
    }

    @Override
    public String getMsg() {
        return msg;
    }

}
