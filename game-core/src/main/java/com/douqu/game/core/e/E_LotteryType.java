public enum E_LotteryType implements BaseEnum {

    ONCE(1,"单次抽"),
    REPEATEDLY(2,"多次抽")
    ;

    int code;

    String msg;

    E_LotteryType(int code, String msg)
    {
        this.code = code;
        this.msg = msg;
    }

    @Override
    public int getCode() {
        return code;
    }

    @Override
    public String getMsg() {
        return msg;
    }
}
